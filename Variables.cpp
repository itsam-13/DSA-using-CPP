#include<iostream>
using namespace std;
int main()
{
    int x;  // Declaring an integer variable named x
    x = 42; // Assigning the value 42 to the variable x
    cout << x << endl; // Printing the value of x
    x = 45; // Updating the value of x to 45
    cout << x << endl; // Printing the updated value of x
    x = x + 10; // Modifying the value of x by adding 10 to it
    cout << x << endl; // Printing the modified value of x
}