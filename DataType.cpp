#include<iostream>
using namespace std;
int main()
{
    int age = 25; // Declaring an integer variable and initializing it with a value
    double pi = 3.14159; // Declaring a double variable and initializing it with a value
    char grade = 'A'; // Declaring a char variable and initializing it with a value
    string name = "Alice"; // Declaring a string variable and initializing it with a value
    float temperature = 36.6f; // Declaring a float variable and initializing it with a value
    bool isStudent = true; // Declaring a boolean variable and initializing it with a value 


    cout << "Age: " << age << endl; // Printing the value of the age variable
    cout << "Pi: " << pi << endl; // Printing the value of the pi variable
    cout << "Grade: " << grade << endl; // Printing the value of the grade variable
    cout << "Name: " << name << endl; // Printing the value of the name variable
    cout << "Temperature: " << temperature << endl; // Printing the value of the temperature variable
    cout << "Is Student: " << isStudent << endl; // Printing the value of the isStudent variable

    


    return 0;
}